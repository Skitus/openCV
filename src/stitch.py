# В данном случае, возможно, лучшим решением было бы использовать некоторый алгоритм удаления фона, чтобы удалить дом из второго изображения, а затем добавить полученное изображение машины к изображению дома.

# Приведу простой пример, как это можно сделать с использованием библиотеки OpenCV для удаления фона и добавления изображений вместе:



import cv2
import numpy as np

# Загрузите изображения
img1 = cv2.imread('/home/artur/Downloads/5/5.JPG')  # изображение с домом
img2 = cv2.imread('/home/artur/Downloads/5/6.JPG')  # изображение с машиной и домом

# Создайте маску для второго изображения
# Это простой пример, который работает, если фон имеет относительно однородный цвет.
mask = cv2.createBackgroundSubtractorMOG2().apply(img2)

# Инвертируйте маску
mask_inv = cv2.bitwise_not(mask)

# Используйте маску для удаления фона из второго изображения
img2_fg = cv2.bitwise_and(img2, img2, mask=mask_inv)

# Используйте инвертированную маску для удаления области машины из первого изображения
img1_bg = cv2.bitwise_and(img1, img1, mask=mask)

# Добавьте два полученных изображения вместе
combined = cv2.add(img1_bg, img2_fg)

# Сохраните итоговое изображение
cv2.imwrite('combined.jpg', combined)
